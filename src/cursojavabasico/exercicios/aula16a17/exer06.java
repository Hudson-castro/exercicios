package cursojavabasico.exercicios.aula16a17;

import java.util.Scanner;

public class exer06 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        for (int i = 1; i <= 20; i++) {
            System.out.print(" " + i);

        }
        for (int i = 1; i <= 20; i++) {
            System.out.println("\n " + i);

        }

        sc.close();
    }
}
